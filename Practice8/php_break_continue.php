<?php

echo "<h1>PHP BREAK AND CONTINUE</h1>";

// PHP Break
echo "<h2>PHP Break</h2>";

for ($x = 0; $x < 10; $x++) {
  if ($x == 4) {
    break;
  }
  echo "The number is: $x <br>";
}

// PHP Continue
echo "<h2>PHP Continue</h2>";

for ($x = 0; $x < 10; $x++) {
  if ($x == 4) {
    continue;
  }
  echo "The number is: $x <br>";
}

// Break and Continue in While Loop
echo "<h2>Break in While Loop</h2>";

$x = 0;

while($x < 10) {
  if ($x == 4) {
    break;
  }
  echo "The number is: $x <br>";
  $x++;
}

echo "<h2>Continue in While Loop</h2>";

while($x < 10) {
  if ($x == 4) {
    $x++;
    continue;
  }
  echo "The number is: $x <br>";
  $x++;
}
?>